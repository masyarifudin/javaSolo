public class ContohVariabel{
	public static void main(String[] args) {
		int angka=7500;
		char huruf='x';
		double desimal=75000.99012;
		float phi=3.14f;
		byte satu, dua, tiga;
		satu = 7;
		dua = 100;
		tiga = 57;

		System.out.println("angkanya 	: "+angka);
		System.out.println("hurufnya	: "+huruf);
		System.out.println("desimalnya	: "+desimal);
		System.out.println("phinya		: "+phi);
		System.out.println("satunya		: "+satu);
		System.out.println("duanya		: "+dua);
		System.out.println("tiganya		: "+tiga);
	}
}